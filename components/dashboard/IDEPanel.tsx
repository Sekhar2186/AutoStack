type FileNode = { name: string; path?: string; isDir: boolean; depth: number; open?: boolean; active?: boolean; children?: FileNode[] };

function FileRow({ node, activeFile, onSelect }: { node: FileNode; activeFile: string; onSelect: (n: string) => void }) {
  const [open, setOpen] = useState(node.open ?? false);
  const isActive = !node.isDir && activeFile === (node.path || node.name);

  return (
    <>
      <button
        onClick={() => node.isDir ? setOpen((v) => !v) : onSelect(node.path || node.name)}
        className={`w-full flex items-center gap-1.5 px-2 py-1 rounded-lg text-[12px] transition-all hover:bg-white/5 ${isActive ? "bg-cyan-500/10 text-cyan-400 border border-cyan-500/20" : "text-slate-400"}`}
        style={{ paddingLeft: `${8 + node.depth * 16}px` }}
      >
        {node.isDir ? (open ? <ChevronDown size={12} className="shrink-0 text-slate-500" /> : <ChevronRight size={12} className="shrink-0 text-slate-500" />) : <File size={12} className="shrink-0 text-slate-600" />}
        <span className="truncate">{node.name}</span>
      </button>
      {node.isDir && open && node.children?.map((child) => (
        <FileRow key={(child.path || child.name) + child.depth} node={child} activeFile={activeFile} onSelect={onSelect} />
      ))}
    </>
  );
}

