function CodeTab({ app, activeFile }: { app: any, activeFile: string }) {
  const [content, setContent] = useState("");
  const [loading, setLoading] = useState(false);
  const [copied, setCopied] = useState(false);

  useEffect(() => {
    if (!app?.projectId || !activeFile) return;
    const fetchFile = async () => {
      setLoading(true);
      try {
        const res = await fetch(`/api/project-files?projectId=${app.projectId}&version=${app.version || "v1"}&file=${encodeURIComponent(activeFile)}`);
        const data = await res.json();
        setContent(data.content || "// File not found");
      } catch (err) {
        setContent("// Error loading file");
      }
      setLoading(false);
    };
    fetchFile();
  }, [app?.projectId, app?.version, activeFile]);

  const copy = () => { navigator.clipboard.writeText(content); setCopied(true); setTimeout(() => setCopied(false), 2000); };

  const lines = content.split('\n').map((v, i) => ({ ln: i + 1, v }));

  return (
    <div className="flex flex-col h-full">
      <div className="flex items-center justify-between px-4 py-2 border-b border-white/6 bg-white/2 shrink-0">
        <span className="px-2.5 py-1 rounded-md bg-cyan-500/10 border border-cyan-500/20 text-[11px] text-cyan-400 font-mono">{activeFile}</span>
        <button onClick={copy} className="px-2.5 py-1.5 rounded-lg glass text-[11px] text-slate-400">
          <Copy size={11} className="inline mr-1" />{copied ? "Copied!" : "Copy"}
        </button>
      </div>
      <div className="flex-1 overflow-auto bg-[#020617]">
        {loading ? (
          <div className="p-8 text-xs text-slate-600 animate-pulse">Fetching from source…</div>
        ) : (
          <pre className="terminal-font text-[12px] leading-6 p-4">
            {lines.map((line) => (
              <div key={line.ln} className="flex hover:bg-white/2">
                <span className="w-10 text-right text-slate-700 mr-5 shrink-0">{line.ln}</span>
                <span className="text-slate-300">{line.v}</span>
              </div>
            ))}
          </pre>
        )}
      </div>
    </div>
  );
}

